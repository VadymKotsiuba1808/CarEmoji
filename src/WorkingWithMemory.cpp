void ReadFile(uint16_t *hArr, int key)
{

    byte bArr[2048];
    String name;
    name = ("/FAV_" + String(key));
    if(!SPIFFS.exists(name)){
        Serial.println("File not found...");
        hArr = DefaultBitmap;
        return;
    }
    Serial.printf("Read name is : %s\n", name);
    myFile = SPIFFS.open(name, FILE_READ);
    myFile.read(bArr, 2048);
    myFile.close();
    // memcpy(hArr, bArr, 2048);
    GetHexArray(hArr, bArr);
}

